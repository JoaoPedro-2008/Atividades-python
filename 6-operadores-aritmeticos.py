# Operadores Aritméticos
# Soma
soma = 10 + 5
print(soma)
# Subtração
subtração = 10 - 5
print(subtração)
# Multiplicação
multiplicacao = 10 * 5
print(multiplicacao)
# Divisão
divisao = 10 / 5
print(divisao)
# Divisão Inteira
divisao_inteira = 10 // 5
print(divisao_inteira)
# Exponenciação
exponenciacao = 10 ** 2
print(exponenciacao)
# Módulo / Resto da Divisão
modulo = 10 % 5
print(modulo)